            'booking_number' => $request->booking_number,
            'email' => $request->email,
            'tel_number' => $request->tel_number,
            'book_time' => $request->book_time,
            'court_id' => $request->courts_id
        ]);

        return to_route('admin.booking.index')->with('success', 'Booking created successfully.');
